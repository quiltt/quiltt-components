/** @function */
/**
 * Formats number to currency string
 * @param {number} value
 * @param {string} [locale=en-US]
 * @param {Object} [options]
 */
export const currencyFormatter = (
  value: number,
  locale: Intl.UnicodeBCP47LocaleIdentifier = 'en-US',
  options: CurrencyFormatterOptions = {
    style: 'currency',
    currency: 'USD',
    currencyDisplay: 'symbol',
    signDisplay: 'auto',
  }
): string => {
  const formatter = new Intl.NumberFormat(locale, options)
  const decimalString = value.toFixed(2)
  const floatNumber = parseFloat(decimalString)
  return formatter.format(floatNumber)
}

type CurrencyDisplay = 'symbol' | 'narrowSymbol' | 'code' | 'name'

type SignDisplay = 'auto' | 'never' | 'always' | 'exceptZero'

type Style = 'decimal' | 'currency' | 'percent' | 'unit'

export type CurrencyFormatterOptions = {
  style?: Style
  currency?: Currency
  currencyDisplay?: CurrencyDisplay
  signDisplay?: SignDisplay
}

